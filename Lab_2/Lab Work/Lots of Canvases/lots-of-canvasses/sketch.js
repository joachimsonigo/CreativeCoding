var sketch1 = function(p){
p.r = p.random(255);
p.g = p.random(255);
p.b = p.random(255);
p.x;
p.y;


      p.setup =function(){

        p.canvas = p.createCanvas(p.windowWidth/5,p.windowHeight/5);
        p.canvas.position(p.random(p.windowWidth), p.random(p.windowHeight));
        p.x = p.random(p.width);
        p.y = p.random(p.height);
        p.frameRate(1);
      }

      p.draw = function(){

        p.background(p.r, p.g, p.b);
        p.fill(p.r);
        p.ellipse(p.x, p.y,50,50);
        p.windowChPos();

     }

    p.windowResized = function(){
     p.resizeCanvas(p.windowWidth/5, p.windowHeight/5);
     }

     p.windowChPos = function(){
       p.canvas.position(p.random(p.windowWidth), p.random(p.windowHeight));
     }
}

